/*
 * reverse_listint - reverses a singly-linked listint
 * @head - pointer to string node in the list
 *
 * Return: pointer to the first node of the list
 */
void reverse_listint(listint_t **head)
{
	listint_t *prev = NULL;
	listint_t *current = *head;
	listint_t *next = NULL;

	while (current)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}

	*head = prev;
}
